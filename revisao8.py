# ORDENAR OS ELEMENTOS DE UMA LISTA (CRESCENTE)
cores = ["azul","verde","cinza", "branco"]
cores.sort()
print(cores)

# ORDEM DECRESCENTE (MAIOR PARA O MENOR)
numeros = [6, 4, 9, 21, 7, 14, 10, 2, 13]
numeros.sort(reverse=True)
print(numeros)

# INVERTER A ORDEM DA LISTA (ESPELHAR)
cores.reverse()
print(cores)

# LIMPAR A LISTA (REMOVER TODOS OS ELEMENTOS)
numeros.clear()
print(numeros)