# ORDENAR OS DADOS DA LISTA
lista = ["Azul","Verde","Marrom","Branco"]
lista.sort()  # ordem alfabética (texto)
print(lista)

numeros = [14, 21, 10, 6, 94, 73]
numeros.sort() # ordem crescente
print(numeros)

# ORDEM DECRESCENTE
numeros.sort(reverse=True) # tb funciona com texto
print(numeros)

# INVERTER SEM ORDENAR
comidas = ["Coxinha","Açaí","Batata Frita","Milkshake"]
comidas.reverse()
print(comidas)