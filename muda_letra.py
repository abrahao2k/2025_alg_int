frase = input("Digite uma frase: ")
print("MAIÚSCULAS:", frase.upper())
print("minúsculas:", frase.lower())
print("primeira maiúscula:", frase.capitalize())
print("primeira de cada:", frase.title())
print("inverter:", frase.swapcase())

# .upper()  - transforma em maiúsculas
# .lower()  - transforma em minúsculas
# .capitalize() - primeira letra da frase maiúscula
# .title() - primeira letra de cada palavra maiuscula
# .swapcase() - inverte maiúsculas/minúsculas

