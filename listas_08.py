# remover pela posição
# pop() - sem nada nos parênteses, remove o último item
# pop(1) - com número no parênteses, remove a posição indicada
frutas = ["Morango","Cajá", "Banana", "Kiwi", "Melão", "Goiaba"]
print(frutas)

frutas.pop()
print(frutas)

frutas.pop()
print(frutas)

frutas.pop(2)  # remove a posição 2
print(frutas)

frutas.pop(0) # remove a posição 0
print(frutas)