# SUM - somar os elementos de uma lista
salarios = [1500, 2700, 3100, 1975, 2320]
print("Total dos salários:", sum(salarios) )

# CALCULAR A MÉDIA DOS VALORES DA LISTA
print("Média dos salários:", sum(salarios) / len(salarios) )

# MAIOR VALOR DE UMA LISTA
print("Maior salário:", max(salarios) )

# MENOR VALOR DE UMA LISTA
print("Menor salário:", min(salarios) )

# CONTAR A QUANTIDADE DE VEZES QUE UM ELEMENTO APARECE NA LISTA
respostas = ['s', 'n', 's', 's', 'n', 's', 'n', 'n', 'n', 'n']

print("Qtd de respostas SIM: ", respostas.count('s') )
print("Qtd de respostas NÃO: ", respostas.count('n') )